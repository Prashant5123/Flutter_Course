// Write a dart program to print the color of the rainbow based on the input.
// (The color of rainbow are Voilet, Indigo, Blue, Green , Yellow, Orange Red)

void main(){
  int x=3;
  if(x==1){
    print("Voilet");
  }else if(x==2){
    print("Indigo");
  }else if(x==3){
    print("Blue");
  }else if(x==4){
    print("Green");
  }else if(x==5){
    print("Yellow");
  }else if(x==2){
    print("Orange");
  }else{
    print("Red");
  }
}